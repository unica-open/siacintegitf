package it.csi.siac.siacintegser.frontend.webservice.msg.ricerche.soggetto;

import it.csi.siac.siacintegser.frontend.webservice.msg.base.ricerche.BaseRicercaRequest;

public abstract class BaseRicercaSoggetti extends BaseRicercaRequest {
	private String codice;
	private String partitaIva;
	private String codiceFiscale;

	public String getPartitaIva() {
		return partitaIva;
	}

	public void setPartitaIva(String partitaIva) {
		this.partitaIva = partitaIva;
	}

	public String getCodiceFiscale() {
		return codiceFiscale;
	}

	public void setCodiceFiscale(String codiceFiscale) {
		this.codiceFiscale = codiceFiscale;
	}

	public String getCodice() {
		return codice;
	}

	public void setCodice(String codice) {
		this.codice = codice;
	}

	
}
